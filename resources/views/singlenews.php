                            <p><strong>PETALING JAYA:</strong>
                                Top Glove’s wholly owned subsidiary and research & development (R&D) unit, Top Glove International Sdn Bhd, has been ranked second among Malaysian patent applicants in 2020, according to the Intellectual Property Corporaton of Malaysia (MyIPO) top 20 list of patent applicants.</p>
                                <p>Top Glove said the significance of intellectual property (IP) has always been appreciated given its importance in an increasingly knowledge driven economy as well as the contribution to the country’s continuous development.</p>
                                <p>“We have always believed in doing well by doing good and remaining committed to managing our business in a way that respects and contributes positively to the environment, resources, and greater community,” said the group in a press statement.</p>
                                <p>Top Glove has nine R&D centres throughout Malaysia and Thailand, which collectively employ an estimated 1,000 researchers, mainly engineers, chemists and scientists.</p>
                                <p>It said the centres take on a multidisciplinary approach to create new development for its gloves and other personal protective equipment, which includes product and material development; developing sustainable technologies; advancing engineering and automation; the innovation and development of formers, as well as the filing of IPs; and talent development and collaboration.</p>
                                <p>In 2020, the R&D centres filed 52 patent applications in Malaysia and 97 patent applications overseas related to gloves, formers, dental dams, chemicals used in the glove industry and automation systems.</p>
                                <p>It pointed out that patent filings had traditionally been dominated by universities, but its high ranking reflects the trend towards industry based R&D and its commitment towards inventions, innovation and continuous enhancement.</p>
                                <p>Top Glove stated that it will continue to recruit more talent for its growing R&D team to advance its journey towards Industry 4.0.</p>
                                <p>On the whole, it owns more than 300 IPs domestically and abroad, including brands such as Top Care, GripPlus, Finesses, Flexylon, Top Feel, Biogreen, and Hydraplus.</p>
