                                <p><strong>Dato’ Lee Kim Meow, Managing Director of Top Glove, commented:</strong> “As health awareness remains high and economies worldwide gradually recover, we are dedicated to pursuing technology driven improvement initiatives which emphasise on increasing quality, efficiency, and innovation across Top Glove’s production processes, digital and data analysis systems, and product range. An additional primary listing can provide the Group with an expanded platform to finance and accelerate these initiatives.”</p>
                                <p>It is intended that a portion of the net proceeds raised from the Proposed Dual Primary Listing would be invested in Top Glove’s Environmental, Social and Corporate Governance practices and initiatives. The actual amount of proceeds to be raised from the Proposed Dual Primary Listing will depend on the number of new Top Glove shares to be issued and the final issue price of the new Top Glove shares pursuant to the Proposed Dual Primary Listing.</p>
                                <p>Subject to completion of the Proposed Dual Primary Listing, Top Glove shares will be listed and traded on Bursa Securities, SGX-ST, and HKEX and all such shares will be fungible/transferable among these three stock exchanges. Details regarding the process to transfer shares among these three stock exchanges and the fees payable will be provided in due course.</p>
